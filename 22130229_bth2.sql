SELECT GV.MAGV, GV.HOTEN
FROM GIAOVIEN GV
WHERE NOT EXISTS (
    SELECT *
    FROM MONHOC MH
    WHERE MH.MAKHOA = 'QL'
    AND NOT EXISTS (
        SELECT *
        FROM GIANGDAY GD
        WHERE GD.MAMH = MH.MAMH
        AND GD.MAGV = GV.MAGV
    )
);

SELECT HOCVIEN.HO+'' +HOCVIEN.TEN AS HoTenLopTruong
FROM LOP
INNER JOIN HOCVIEN ON LOP.TRGLOP = HOCVIEN.MAHV
WHERE LOP.SISO = (SELECT MAX(SISO) FROM LOP);

SELECT DISTINCT HV.HO + ' ' + HV.TEN AS HoTenLopTruong
FROM HOCVIEN HV
INNER JOIN LOP ON HV.MALOP = LOP.MALOP
WHERE HV.MALOP IN (
    SELECT MALOP
    FROM GIANGDAY
    GROUP BY MALOP
    HAVING COUNT(*) = 3
)
AND HV.MAHV NOT IN (
    SELECT MAHV
    FROM KETQUATHI
    WHERE KQUA = 'Khong Dat'
    GROUP BY MAHV
    HAVING COUNT(DISTINCT MAMH) = 3
);

SELECT HV.MAHV, HV.HO + ' ' + HV.TEN AS HoTen
FROM HOCVIEN HV
INNER JOIN KETQUATHI KQ ON HV.MAHV = KQ.MAHV
WHERE KQ.DIEM >= 9
GROUP BY HV.MAHV, HV.HO, HV.TEN
HAVING COUNT(*) = (
    SELECT COUNT(*)
    FROM KETQUATHI
    WHERE MAHV = HV.MAHV
)
ORDER BY COUNT(*) DESC;

SELECT HV.MAHV, HV.HO + ' ' + HV.TEN AS HoTen
FROM HOCVIEN HV
INNER JOIN (
    SELECT MAHV, COUNT(*) AS SoMonDat910
    FROM KETQUATHI
    WHERE DIEM >= 9
    GROUP BY MAHV
) AS MonDat910 ON HV.MAHV = MonDat910.MAHV
INNER JOIN (
    SELECT MALOP, MAX(SoMonDat910) AS MaxSoMonDat910
    FROM (
        SELECT LOP.MALOP, HV.MAHV, COUNT(*) AS SoMonDat910
        FROM LOP
        INNER JOIN HOCVIEN HV ON LOP.MALOP = HV.MALOP
        INNER JOIN KETQUATHI KQ ON HV.MAHV = KQ.MAHV
        WHERE KQ.DIEM >= 9
        GROUP BY LOP.MALOP, HV.MAHV
    ) AS Temp
    GROUP BY MALOP
) AS MaxMonDat910 ON HV.MALOP = MaxMonDat910.MALOP AND MonDat910.SoMonDat910 = MaxMonDat910.MaxSoMonDat910
ORDER BY HV.MALOP;

SELECT GV.MAGV, GV.HOTEN AS TenGiaoVien, 
       GD.HOCKY, GD.NAM,
       COUNT(DISTINCT GD.MAMH) AS SoMonHoc,
       COUNT(DISTINCT GD.MALOP) AS SoLop
FROM GIANGDAY GD
INNER JOIN GIAOVIEN GV ON GD.MAGV = GV.MAGV
GROUP BY GV.MAGV, GV.HOTEN, GD.HOCKY, GD.NAM
ORDER BY GD.NAM, GD.HOCKY, GV.MAGV;

SELECT GD.HOCKY, GD.NAM, GV.MAGV, GV.HOTEN AS TenGiaoVien
FROM GIANGDAY GD
INNER JOIN GIAOVIEN GV ON GD.MAGV = GV.MAGV
GROUP BY GD.HOCKY, GD.NAM, GV.MAGV, GV.HOTEN
HAVING COUNT(*) = (
    SELECT MAX(TotalCount)
    FROM (
        SELECT COUNT(*) AS TotalCount
        FROM GIANGDAY
        WHERE HOCKY = GD.HOCKY AND NAM = GD.NAM
        GROUP BY MAGV
    ) AS SubQuery
)
ORDER BY GD.NAM, GD.HOCKY;

