SELECT HV.MAHV, HV.HO + ' ' + HV.TEN AS TenHocVien, KT.MAMH, MH.TENMH
FROM HOCVIEN HV
INNER JOIN KETQUATHI KT ON HV.MAHV = KT.MAHV
INNER JOIN MONHOC MH ON KT.MAMH = MH.MAMH
WHERE KT.LANTHI = 1 AND KT.KQUA = 'Dat'
GROUP BY HV.MAHV, HV.HO, HV.TEN, KT.MAMH, MH.TENMH;

SELECT DISTINCT KQ.MAHV, HV.HO + ' ' + HV.TEN AS TenHocVien
FROM KETQUATHI KQ
INNER JOIN (
    SELECT MAHV, MAMH, MAX(LANTHI) AS MaxLanThi
    FROM KETQUATHI
    GROUP BY MAHV, MAMH
) MaxLan ON KQ.MAHV = MaxLan.MAHV AND KQ.MAMH = MaxLan.MAMH AND KQ.LANTHI = MaxLan.MaxLanThi
JOIN HOCVIEN HV ON KQ.MAHV = HV.MAHV
WHERE KQ.KQUA = 'Dat';

SELECT KQ.MAHV, HV.HO + ' ' + HV.TEN AS TenHocVien, KQ.MAMH, KQ.DIEM
FROM KETQUATHI KQ
INNER JOIN (
    SELECT MAHV, MAMH, MAX(LANTHI) AS LANTHI
    FROM KETQUATHI
    GROUP BY MAHV, MAMH
) LastExam ON KQ.MAHV = LastExam.MAHV AND KQ.MAMH = LastExam.MAMH AND KQ.LANTHI = LastExam.LANTHI
INNER JOIN HOCVIEN HV ON KQ.MAHV = HV.MAHV;