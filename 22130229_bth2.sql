-- Câu C
SELECT * FROM KHOA WHERE MONTH(NGTLAP) >= 6 AND YEAR(NGTLAP) = 2005;

SELECT * FROM KHOA WHERE TRGKHOA IS NULL;

SELECT * FROM KHOA WHERE TENKHOA LIKE 'k%h';

SELECT * FROM KHOA WHERE NGTLAP = (SELECT MIN(NGTLAP) FROM KHOA);

SELECT * FROM GIAOVIEN WHERE MUCLUONG = (SELECT MIN(MUCLUONG) FROM GIAOVIEN);

SELECT * FROM GIAOVIEN
WHERE HOCVI = 'TS' AND DATEDIFF(YEAR, NGSINH, GETDATE()) < 50;

SELECT * FROM GIAOVIEN WHERE GIOITINH = 'Nam' AND MUCLUONG > 1800000;

SELECT TOP 1 * FROM GIAOVIEN ORDER BY NGSINH ASC;

SELECT * FROM HOCVIEN
WHERE GIOITINH = 'Nu' AND NOISINH = 'Vinh Long' AND DATEDIFF(YEAR, NGSINH, GETDATE()) > 30;

SELECT TOP 1 * FROM HOCVIEN ORDER BY NGSINH DESC;

SELECT H.MAHV, H.HO + ' ' + H.TEN AS HOTEN, H.NGSINH, L.MALOP
FROM HOCVIEN H
INNER JOIN LOP L ON H.MAHV = L.TRGLOP;

SELECT H.MAHV, H.HO + ' ' + H.TEN AS HOTEN, K.LANTHI, K.DIEM
FROM HOCVIEN H
INNER JOIN KETQUATHI K ON H.MAHV = K.MAHV
INNER JOIN MONHOC M ON K.MAMH = M.MAMH
WHERE M.TENMH = 'CSDL'
ORDER BY H.HO, H.TEN;

SELECT H.MAHV, H.HO + ' ' + H.TEN AS HOTEN, K.DIEM
FROM HOCVIEN H
INNER JOIN (
    SELECT MAHV, DIEM
    FROM (
        SELECT MAHV, DIEM, ROW_NUMBER() OVER (PARTITION BY MAHV ORDER BY NGTHI DESC) AS RowNum
        FROM KETQUATHI K
        INNER JOIN MONHOC M ON K.MAMH = M.MAMH
        WHERE M.TENMH = 'CSDL'
    ) AS T
    WHERE RowNum = 1
) AS K ON H.MAHV = K.MAHV;

SELECT H.MAHV, H.HO + ' ' + H.TEN AS HOTEN, K.DIEM
FROM HOCVIEN H
INNER JOIN (
    SELECT MAHV, MAX(DIEM) AS MaxDiem
    FROM KETQUATHI K
    INNER JOIN MONHOC M ON K.MAMH = M.MAMH
    WHERE M.TENMH = 'Co So Du Lieu'
    GROUP BY MAHV
) AS MaxDiemSubquery ON H.MAHV = MaxDiemSubquery.MAHV
INNER JOIN KETQUATHI K ON H.MAHV = K.MAHV AND K.DIEM = MaxDiemSubquery.MaxDiem
INNER JOIN MONHOC M ON K.MAMH = M.MAMH AND M.TENMH = 'Co So Du Lieu';

SELECT H.MAHV, H.HO +' '+ H.TEN AS HOTEN
FROM HOCVIEN H
INNER JOIN KETQUATHI K ON H.MAHV = K.MAHV
WHERE K.MAMH = 'CTRR' AND K.LANTHI = 1 AND K.KQUA = 'Khong Dat';

SELECT DISTINCT M.TENMH FROM MONHOC M
INNER JOIN GIANGDAY GD ON M.MAMH = GD.MAMH
INNER JOIN GIAOVIEN GV ON GD.MAGV = GV.MAGV
WHERE GV.HOTEN = 'Tran Tam Thanh' AND GD.HOCKY = 1 AND  YEAR(GD.TUNGAY) = 2006;

SELECT DISTINCT M.MAMH, M.TENMH
FROM GIAOVIEN GV
INNER JOIN LOP L ON GV.MAGV = L.MAGVCN
INNER JOIN GIANGDAY GD ON L.MALOP = GD.MALOP
INNER JOIN MONHOC M ON GD.MAMH = M.MAMH
WHERE L.TENLOP = 'K11' AND GD.HOCKY = 1 AND GD.NAM = 2006;

SELECT DISTINCT HV.HO + ' ' + HV.TEN AS HOTEN_LOP_TRUONG
FROM GIAOVIEN GV
JOIN LOP L ON GV.MAGV = L.MAGVCN
JOIN GIANGDAY GD ON L.MALOP = GD.MALOP
JOIN MONHOC MH ON GD.MAMH = MH.MAMH
JOIN HOCVIEN HV ON L.TRGLOP = HV.MAHV
WHERE GV.HOTEN = 'Nguyen To Lan' AND MH.TENMH = 'Co So Du Lieu';

SELECT DISTINCT M.MAMH, M.TENMH
FROM DIEUKIEN D
JOIN MONHOC M ON D.MAMH_TRUOC = M.MAMH
WHERE D.MAMH = (SELECT MAMH FROM MONHOC WHERE TENMH = 'Co So Du Lieu');

SELECT DISTINCT M.MAMH, M.TENMH
