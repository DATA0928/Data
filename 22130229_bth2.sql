SELECT DISTINCT GV.HOTEN
FROM GIAOVIEN GV
JOIN GIANGDAY GD ON GV.MAGV = GD.MAGV
JOIN MONHOC MH ON GD.MAMH = MH.MAMH
JOIN LOP L ON GD.MALOP = L.MALOP
WHERE MH.MAMH = 'CTRR'
AND L.TENLOP IN ('K11', 'K12')
AND GD.HOCKY = 1
AND GD.NAM = 2006;

SELECT L.MALOP, L.TENLOP, COUNT(H.MAHV) AS SoLuongHocVien
FROM LOP L
LEFT JOIN HOCVIEN H ON L.MALOP = H.MALOP
GROUP BY L.MALOP, L.TENLOP
ORDER BY SoLuongHocVien DESC;

SELECT MAKHOA, COUNT(*) AS SoLuongGiaoVien
FROM GIAOVIEN
WHERE HOCVI IN ('CN', 'KS', 'ThS', 'TS', 'PTS')
GROUP BY MAKHOA;

SELECT CASE
           WHEN YEAR(GETDATE()) - YEAR(NGSINH) > 60 THEN 'Cao tuoi'
           WHEN YEAR(GETDATE()) - YEAR(NGSINH) BETWEEN 40 AND 60 THEN 'Trung binh'
           ELSE 'Tre'
       END AS DoTuoi,
       COUNT(*) AS SoLuongGiaoVien
FROM GIAOVIEN
GROUP BY CASE
             WHEN YEAR(GETDATE()) - YEAR(NGSINH) > 60 THEN 'Cao tuoi'
             WHEN YEAR(GETDATE()) - YEAR(NGSINH) BETWEEN 40 AND 60 THEN 'Trung binh'
             ELSE 'Tre'
         END;

