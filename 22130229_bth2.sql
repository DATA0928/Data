FROM MONHOC
INNER JOIN KETQUATHI ON MONHOC.MAMH = KETQUATHI.MAMH
WHERE KETQUATHI.LANTHI = 1 AND KETQUATHI.KQUA = 'Khong Dat'
GROUP BY MONHOC.MAMH, MONHOC.TENMH 
